#include "ft_printf.h"

void	ft_skip_nbr(void)
{
	while (*g_cur && ft_isspace(*g_cur))
		++g_cur;
	if (*g_cur == '+' || *g_cur == '-')
		++g_cur;
	while (*g_cur && ft_isdigit(*g_cur))
		++g_cur;
}
